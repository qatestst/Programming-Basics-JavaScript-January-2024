// Функция, която приема сума - рестото, което трябва да се върне
// и изчислява с колко най-малко монети може да стане това
function solve(input) {
    // Преобразуваме входа в число
    let amount = Number(input[0]);
    // Създаваме масив със стойностите на монетите в лева
    let coins = [2, 1, 0.5, 0.2, 0.1, 0.05, 0.02, 0.01];
    // Създаваме променлива за броя на монетите
    let count = 0;
    // Обхождаме масива с монетите
    for (let coin of coins) {
      // Докато сумата е по-голяма или равна на стойността на монетата
      while (amount >= coin) {
        // Намаляваме сумата със стойността на монетата
        amount -= coin;
        // Увеличаваме броя на монетите с единица
        count++;
        // Закръгляме сумата до втория знак след десетичната запетая
        // за да избегнем грешки при сравнението
        amount = Math.round(amount * 100) / 100;
      }
    }
    // Връщаме броя на монетите
    return count;
  }

  
  