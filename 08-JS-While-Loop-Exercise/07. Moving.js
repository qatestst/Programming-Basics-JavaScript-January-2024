// JS функция solve(input), която приема масив от входни данни
function solve(input) {
    // Прочитаме размерите на свободното пространство от първите три елемента на масива
    let width = Number(input[0]); // широчина на свободното пространство
    let length = Number(input[1]); // дължина на свободното пространство
    let height = Number(input[2]); // височина на свободното пространство
    // Изчисляваме обема на свободното пространство
    let volume = width * length * height; // общ обем в кубични метри
    // Започваме да четем от четвъртия елемент на масива
    let index = 3; // индекс на текущия елемент
    // Докато има още данни в масива и обемът не е изчерпан
    while (index < input.length && volume > 0) {
      // Прочитаме текущия елемент
      let command = input[index]; // текуща команда
      // Ако командата е "Done", прекъсваме цикъла
      if (command === "Done") {
        break;
      }
      // В противен случай, преобразуваме командата в число
      let boxes = Number(command); // брой кашони
      // Намаляваме обема с обема на кашоните
      volume -= boxes;
      // Увеличаваме индекса за следващия елемент
      index++;
    }
    // Проверяваме дали обемът е изчерпан
    if (volume < 0) {
      // Ако е изчерпан, отпечатваме колко кубични метра не достигат
      console.log(`No more free space! You need ${Math.abs(volume)} Cubic meters more.`);
    } else {
      // Ако не е изчерпан, отпечатваме колко кубични метра са останали
      console.log(`${volume} Cubic meters left.`);
    }
  }
  