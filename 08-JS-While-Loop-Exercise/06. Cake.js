// JS функция solve(input), която приема масив от входни данни
function solve(input) {
    // Прочитаме размерите на тортата от първите два елемента на масива
    let width = Number(input[0]); // широчина на тортата
    let length = Number(input[1]); // дължина на тортата
    // Изчисляваме броя на парчетата торта
    let pieces = width * length; // общ брой парчета
    // Започваме да четем от третия елемент на масива
    let index = 2; // индекс на текущия елемент
    // Докато има още данни в масива и парчетата торта не са свършили
    while (index < input.length && pieces > 0) {
      // Прочитаме текущия елемент
      let command = input[index]; // текуща команда
      // Ако командата е "STOP", прекъсваме цикъла
      if (command === "STOP") {
        break;
      }
      // В противен случай, преобразуваме командата в число
      let takenPieces = Number(command); // брой взети парчета
      // Намаляваме броя на останалите парчета с броя на взетите
      pieces -= takenPieces;
      // Увеличаваме индекса за следващия елемент
      index++;
    }
    // Проверяваме дали парчетата торта са свършили
    if (pieces <= 0) {
      // Ако са свършили, отпечатваме колко парчета не достигат
      console.log(`No more cake left! You need ${Math.abs(pieces)} pieces more.`);
    } else {
      // Ако не са свършили, отпечатваме колко парчета са останали
      console.log(`${pieces} pieces are left.`);
    }
  }
  