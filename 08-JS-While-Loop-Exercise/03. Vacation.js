function solve(input) {
  // Прочитаме първите два елемента на масива, които са парите, нужни за екскурзията, и наличните пари
  let moneyNeeded = Number(input[0]);
  let moneyAvailable = Number(input[1]);

  // Създаваме променливи за броя на дните и броя на последователните дни, в които харчим
  let days = 0;
  let spendDays = 0;

  // Започваме да обхождаме масива от третия елемент нататък, като прескачаме по два елемента на всяка стъпка
  for (let i = 2; i < input.length; i += 2) {
    // Увеличаваме броя на дните с единица
    days++;

    // Проверяваме какво е действието - spend или save
    let action = input[i];

    // Проверяваме каква е сумата, която ще спестим или похарчим
    let amount = Number(input[i + 1]);

    // Ако действието е spend
    if (action === "spend") {
      // Изваждаме сумата от наличните пари
      moneyAvailable -= amount;

      // Ако наличните пари станат по-малко от нула, ги правим равни на нула
      if (moneyAvailable < 0) {
        moneyAvailable = 0;
      }

      // Увеличаваме броя на последователните дни, в които харчим, с единица
      spendDays++;
    } else if (action === "save") {
      // Добавяме сумата към наличните пари
      moneyAvailable += amount;

      // Нулираме броя на последователните дни, в които харчим
      spendDays = 0;
    }

    // Проверяваме дали наличните пари са достатъчни за екскурзията
    if (moneyAvailable >= moneyNeeded) {
      // Ако да, прекратяваме цикъла и извеждаме съобщение, че сме спестили парите за определен брой дни
      console.log(`You saved the money for ${days} days.`);
      break;
    }

    // Проверяваме дали последователните дни, в които харчим, са пет
    if (spendDays === 5) {
      // Ако да, прекратяваме цикъла и извеждаме съобщение, че не можем да спестим парите и колко дни сме изминали
      console.log("You can't save the money.");
      console.log(days);
      break;
    }
  }
}
